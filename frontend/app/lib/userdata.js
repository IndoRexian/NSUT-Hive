export function generateAvatar(name, size) {
    const random_num = Math.floor(Math.random() * 4);
    const key = Object.keys(collection)[random_num];
    const style = collection[key];
    const avatar = createAvatar(style, {
        seed: name,
        size: size,
    });
    const data = {
        style: key,
        avatar: avatar.toDataUri(),
    };
    return data;
}
